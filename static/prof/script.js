; (async () => {
    document.getElementsByClassName("loader")[0].style.display = "block"
    const palette = await getJson("/palette/palettes.json")
    const base = await getJson("/api/v1/profsBases/")
    document.getElementsByClassName("loader")[0].style.display = "none"
    console.log(base)
    let semaine = base["currentWeek"];
    const profs = base["profs"]
    profs.sort((a, b) => a.localeCompare(b))
    const semaineNom = base["weeks"]
    // let EDT = clone(orgEDT) // variable qui stocke tout l'EDTA qui sera à consulter
    var profSelect = document.getElementById("nomProf")
    console.log("hein ?")
    for (let i = 0; i < profs.length; i++) { // affichage primitif pour le choix des groupes
        const opt = document.createElement("option")
        opt.value = profs[i - 1]
        opt.innerText = profs[i - 1]
        profSelect.appendChild(opt)
    }

    const cookiegrp = getCookie("salle")
    if (cookiegrp == "" || (Number(cookiegrp) < 17 && Number(cookiegrp) > 0)) {
        profSelect.value = cookiegrp
    } else profSelect.value = ""

    var semaines = document.getElementById("semaine")
    for (let i = 3; i < 19; i++) {

        const opt = document.createElement("option")
        opt.value = i.toString()
        opt.innerText = i
        semaines.appendChild(opt)
    }
    var paletteElem = document.getElementById("palette")

    const ajusteDate = (n) => {
        return n < 10 ? "0" + n : n
    }
    // --------------------------------------------------------

    let tableauInfo = []

    let salle = profSelect.value;
    
    semaines.value = semaine

    const metNumJours = (fullDays) => {
        const ElemEDT = document.getElementById("EDT").children[0].children
        for (let i = 1; i < ElemEDT.length; i++) {
            const element = ElemEDT[i];
            element.innerText = fullDays[i-1]
        }
    }

    const testparams = () => {
        return salle != 0
    }
    // affichage palette
    Object.keys(palette).forEach(name => {
        const option = document.createElement("option")
        option.value = name
        option.innerText = name
        paletteElem.appendChild(option)
    })

    const setpalette = () => {
        const palcook = getCookie("palette")
        if (palcook == "" || Object.keys(palette).indexOf(palcook) == -1) {
            paletteElem.value = "Guillaume"
            setCookie("palette", "Guillaume", 100)
        } else {
            paletteElem.value = palcook
        }

        const namePal = paletteElem.value
        const pal = palette[namePal]
        if (typeof pal == "undefined" || pal == null) return
        Object.keys(pal).forEach(matiere => {
            const mm = document.getElementsByClassName(matiere)
            for (let i = 0; i < mm.length; i++) {
                const element = mm[i];
                element.style.background = pal[matiere]
            }
        })
    }

    const updateSemaines = async () => {
        document.getElementsByClassName("alert")[0].style.display = "none"
        if (semaine > 18) semaine = 3
        if (semaine < 3) semaine = 18
        semaines.value = semaine
        
        if (testparams() == false) return
        document.getElementsByClassName("loader")[0].style.display = "block"
        const all = await getJson("/api/v1/prof/?prof=" + salle+"&week="+semaine)
        document.getElementsByClassName("loader")[0].style.display = "none"
        if (!all["ok"]) {
            alert("Erreur server (" + all["error"]+")")
        }
        console.log(all)
        afficheEDT(all["EDT"])
        let test = detectOverlap(all["EDT"])
        if (test.length > 0) {
            console.log(document.getElementsByClassName("alert")[0])
            document.getElementsByClassName("alert")[0].style.display = "block"
        }
        const nEDT = all["EDT"]

        metNumJours(all["fullDays"])
        setpalette()
    }

    const changementPourEdt = () => { // fons d'écran spécialisé pour mayeul et évariste
        resetEDT()
        salle = profSelect.value
        updateSemaines()

    }

    semaines.onchange = e => {
        semaine = e.target.value;
        if (testparams() == false) {
            txt.innerText = "Paramètres invalides (Renseignez un prof et une semaine)"
            return
        }
        updateSemaines()
    }
    document.getElementById("nextWeek").onclick = e=> {
        e.preventDefault()
        semaine++
        updateSemaines()
    }

    document.getElementById("prevWeek").onclick = e => {
        e.preventDefault()
        semaine--
        updateSemaines()
    }


    changementPourEdt()

    profSelect.onchange = e => {
        setCookie("salleholle", e.target.value, 100)
        changementPourEdt()
    }
    paletteElem.onchange = e => {
        setCookie("palette", paletteElem.value, 100)
        updateSemaines()
    }

    // ajout des dates semaines pour sélection.
    semaines.childNodes.forEach(elem => {
        const base = semaineNom[elem.value - 2].split("/");
        let init = (new Date(Number("20" + base[2]), Number(base[1]) - 1, Number(base[0]))).getTime()
        let start = new Date(init)
        let end = new Date(init + 4 * 24 * 3600 * 1000)
        elem.innerText = "n°"+elem.value + " : " +ajusteDate(start.getDate()) + "/" + ajusteDate(start.getMonth() + 1) + " - " + ajusteDate(end.getDate()) + "/" + ajusteDate(end.getMonth() + 1)
    })
})()