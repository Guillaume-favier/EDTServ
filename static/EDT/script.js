var selectGrp = document.getElementById("grpKhole")
for (let i = 1; i <= 16; i++) { // affichage primitif pour le choix des groupes
    const opt = document.createElement("option")
    opt.value = i.toString()
    opt.innerText = "G" + i 
    selectGrp.appendChild(opt)
}

const cookiegrp = getCookie("GroupeKholle")
if (cookiegrp == "" || (Number(cookiegrp) < 17 && Number(cookiegrp) > 0)) {
    selectGrp.value = cookiegrp
} else selectGrp.value = ""

var semaines = document.getElementById("semaine")
for (let i = 3; i < 19; i++) {

    const opt = document.createElement("option")
    opt.value = i.toString()
    opt.innerText = i
    semaines.appendChild(opt)
}
var palletteElem = document.getElementById("pallette")

const ajusteDate = (n) => {
    return n < 10 ? "0"+ n : n
}

const txt = document.getElementById("outTxt")

; (async () => {
    document.getElementsByClassName("loader")[0].style.display = "block"
    const pallette = await getJson("palettes.json")
    const base = await getJson("/api/v1/base/")
    document.getElementsByClassName("loader")[0].style.display = "none"
    console.log(base)
    let semaine = base["currentWeek"];
    const groupesPers = base["groupes"]
    const semaineNom = base["weeks"]
    // let EDT = clone(orgEDT) // variable qui stocke tout l'EDTA qui sera à consulter
    txt.innerHTML = "Traitement des données ..."
    let tableauInfo = []

    let groupeK = selectGrp.value == "" ? 0 : Number(selectGrp.value);
    
    semaines.value = semaine

    const metNumJours = (fullDays) => {
        const ElemEDT = document.getElementById("EDT").children[0].children
        for (let i = 1; i < ElemEDT.length; i++) {
            const element = ElemEDT[i];
            element.innerText = fullDays[i-1]
        }
    }

    txt.innerHTML = "Veuillez choisir un groupe"
    const testparams = () => {
        return groupeK != 0
    }
    
    // affichage pallette
    Object.keys(pallette).forEach(name => {
        const option = document.createElement("option")
        option.value = name
        option.innerText = name
        palletteElem.appendChild(option)
    })

    const setPallette = () => {
        const palcook = getCookie("pallette")
        if (palcook == "" || Object.keys(pallette).indexOf(palcook) == -1) {
            palletteElem.value = "Guillaume"
            setCookie("pallette", "Guillaume", 100)
        } else {
            palletteElem.value = palcook
        }

        const namePal = palletteElem.value
        const pal = pallette[namePal]
        if (typeof pal == "undefined" || pal == null) return
        Object.keys(pal).forEach(matiere => {
            const mm = document.getElementsByClassName(matiere)
            for (let i = 0; i < mm.length; i++) {
                const element = mm[i];
                element.style.background = pal[matiere]
            }
        })
    }

    const updateSemaines = async () => {
        let ele = document.getElementById("persGrp");
        ele.innerHTML = ""
        document.getElementById("DownEDT").onclick = () => {}
        if (groupeK == "") {
            txt.innerText = "Veuillez choisir un groupe de kholle"
            return
        }
        txt.innerText = ""
        if (semaine > 18) semaine = 3
        if (semaine < 3) semaine = 18
        semaines.value = semaine
        
        if (testparams() == false) return
        document.getElementsByClassName("loader")[0].style.display = "block"
        const all = await getJson("/api/v1/all/?group=" + groupeK+"&week="+semaine)
        document.getElementsByClassName("loader")[0].style.display = "none"
        if (!all["ok"]) {
            alert("Erreur server (" + all["error"]+")")
        }
        console.log(all)
        afficheEDT(all["EDT"])
        const nEDT = all["EDT"]

        metNumJours(all["fullDays"])
        setPallette()
        // affichage des kholles sous forme de liste
        let ul = document.createElement("ul")
        console.log(all)
        all["kholles"].forEach((days,jour) => {
            for (let i = 0; i < days.length; i++) {
                const kh = days[i];
                const li = document.createElement("li")
                li.innerText = kh[0] + ", le " + jours[jour] + " à  " + (typeof kh[3] == typeof 2 ? (kh[3] + "h") : kh[3]) + " en " + kh[2] + " avec " + kh[5]
                ul.appendChild(li)
            }
        })
        txt.appendChild(ul)

        // affichage des membres du groupe en bas de la page
        let p = document.createElement("p")
        p.innerText = "Personnes du groupe :"
        all["membres"].forEach(pers => {
            p.innerText += "\t " + pers[1] + " " + pers[0] + ". ," 
        })
        p.innerText = p.innerText.substring(0, p.innerText.length - 2)
        ele.appendChild(p)
    }

    const changementPourEdt = () => { // fons d'écran spécialisé pour mayeul et évariste
        resetEDT()
        groupeK = Number(selectGrp.value)
        updateSemaines()
        if (groupeK == 6){
            document.body.className = "manchot"
        }
        else if (groupeK == 4) {
            document.body.className = "chateauuu"
        }
        else {
            document.body.className = ""
        }

    }

    semaines.onchange = e => {
        semaine = e.target.value;
        if (testparams() == false) {
            txt.innerText = "Paramètres invalides"
            return
        }
        updateSemaines()
    }
    document.getElementById("nextWeek").onclick = e=> {
        e.preventDefault()
        semaine++
        updateSemaines()
    }

    document.getElementById("prevWeek").onclick = e => {
        e.preventDefault()
        semaine--
        updateSemaines()
    }


    changementPourEdt()
    // affichage des noms des membres du groupe pour la sélenction
    for (let i = 1; i < selectGrp.children.length; i++) {

        const el = selectGrp.children[i];
        el.innerText += " :  "
        groupesPers[el.value - 1].forEach(pers => {
            el.innerText += pers[1] + " " + pers[0][0]+". ; "
        })
        el.innerText = el.innerText.substring(0, el.innerText.length - 2)
        
    }

    selectGrp.onchange = e => {
        setCookie("GroupeKholle", e.target.value, 100)
        changementPourEdt()
    }
    palletteElem.onchange = e => {
        setCookie("pallette", palletteElem.value, 100)
        updateSemaines()
    }

    // ajout des dates semaines pour sélection.
    semaines.childNodes.forEach(elem => {
        const base = semaineNom[elem.value - 2].split("/");
        let init = (new Date(Number("20" + base[2]), Number(base[1]) - 1, Number(base[0]))).getTime()
        let start = new Date(init)
        let end = new Date(init + 4 * 24 * 3600 * 1000)
        elem.innerText = "n°"+elem.value + " : " +ajusteDate(start.getDate()) + "/" + ajusteDate(start.getMonth() + 1) + " - " + ajusteDate(end.getDate()) + "/" + ajusteDate(end.getMonth() + 1)
    })
})()